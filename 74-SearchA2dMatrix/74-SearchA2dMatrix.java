class Solution {
    public boolean searchMatrix(int[][] matrix, int target) {
        int n=matrix.length;
        int m=matrix[0].length;
        int i=0,j=(m*n )-1;
        while(i<=j){
        int mid =(i+j)/2;
        int val=matrix[mid/m][mid%m];
        if(val == target){
            return true;
        }
        if(val< target){
            i=mid+1;
        }
        else{
            j =mid-1;
        }
        }
        return false;
    }
}